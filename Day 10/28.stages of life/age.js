var age = 20;
if (age < 2) {
    console.log("The person is baby");
}
else if (age < 4) {
    console.log("The person is toddler");
}
else if (age < 13) {
    console.log("The person is kid");
}
else if (age < 20) {
    console.log("The person is teenager");
}
else if (age < 65) {
    console.log("The person is adult");
}
else if (age >= 65) {
    console.log("The person is elder");
}
